
///////////////////(while)///////////////////////////
let contador = 0;

while (contador < 3) {
    console.log("pepe");
    contador++;
}
console.log("fin del ciclo")

////////////////(do while)///////////////////////////

do {
    console.log(contador);
    contador++;

} while (contador < 5);
console.log("fin del ciclo")


/////////////////////(for)///////////////////////////

for (let cont = 0; cont <= 5; cont++) {
    console.log(cont);
}
console.log("fin del ciclo")



