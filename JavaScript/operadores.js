let a = 3, b = 5;


let z = a + b;
console.log("Resultado de la suma :" + z);

z = a + b;
console.log("Resultado de la resta :" + z);

z = a * b;
console.log("Resultado de la multiplicacion :" + z);

z = a / b;
console.log("Resultado de la divicion :" + z);

z = a % b;
console.log("Resultado del modulo :" + z);

z = a ** b;
console.log("Resultado de exponente :" + z);

/////////////////////////////////////////////////////////////

//INCREMENTO    

//PRE-INCREMENTO (EL OPERADOR ++ ANTES DE LA VARIABLE)
z = ++a;
console.log(a);
console.log(z);

//POST-INCREMENTO (EL OPERADOR ++ DESPUES DE LA VARIABLE)

z = b++;
console.log(b);
console.log(z);

//DECREMENTO

//PREDECREMENTO
z = --a;
console.log(a);
console.log(z);

//POST-DECREMENTO
z = b--;
console.log(b);
console.log(z);

